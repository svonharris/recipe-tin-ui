import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const componentsDir = path.join(__dirname, '../src/components');
const outputFile = path.join(__dirname, '../src/index.ts');

function parseExports(content) {
  const typeExports = new Set();
  const valueExports = new Set();

  for (const [, name] of content.matchAll(/^export\s+type\s+(\w+)/gm)) {
    typeExports.add(name);
  }

  for (const [, group] of content.matchAll(/^export\s+type\s+\{\s*([^}]+)\s*\}/gm)) {
    group.split(',').map(n => n.trim()).filter(Boolean).forEach(n => typeExports.add(n));
  }

  for (const [, name] of content.matchAll(/^export\s+(?!default\s)(?:const|function|class)\s+(\w+)/gm)) {
    valueExports.add(name);
  }

  for (const [, group] of content.matchAll(/^export\s+\{\s*([^}]+)\s*\}/gm)) {
    group.split(',').map(n => n.trim()).filter(n => n && n !== 'default').forEach(n => valueExports.add(n));
  }

  return { typeExports: [...typeExports], valueExports: [...valueExports] };
}

const componentDirs = fs.readdirSync(componentsDir, { withFileTypes: true })
  .filter(d => d.isDirectory())
  .map(d => d.name)
  .sort();

const sections = [];

for (const name of componentDirs) {
  const filePath = path.join(componentsDir, name, `${name}.tsx`);
  if (!fs.existsSync(filePath)) continue;

  const content = fs.readFileSync(filePath, 'utf8');
  const { typeExports, valueExports } = parseExports(content);
  const modulePath = `./components/${name}/${name}`;

  const exportLines = [];
  if (valueExports.length > 0) exportLines.push(`export { ${valueExports.join(', ')} } from '${modulePath}'`);
  if (typeExports.length > 0) exportLines.push(`export type { ${typeExports.join(', ')} } from '${modulePath}'`);

  if (exportLines.length > 0) sections.push(exportLines.join('\n'));
}

const output = [
  "// Auto-generated by scripts/generate-component-index.js — do not edit manually",
  "import './styles/index.css'",
  '',
  sections.join('\n\n'),
  '',
].join('\n');

fs.writeFileSync(outputFile, output);
console.log(`Generated src/index.ts with ${sections.length} components`);
